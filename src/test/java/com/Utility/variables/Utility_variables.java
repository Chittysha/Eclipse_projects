package com.Utility.variables;

public class Utility_variables{
	

	private int Age;
	
	private String Name;
	
	private boolean kit;

	public int getAge() {
		return Age;
	}

	public String getName() {
		return Name;
	}

	public boolean isKit() {
		return kit;
	}

	public void setAge(int age) {
		Age = age;
	}

	public void setName(String name) {
		Name = name;
	}

	public void setKit(boolean kit) {
		this.kit = kit;
	}


}
